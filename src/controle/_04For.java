package controle;

public class _04For {
	

        public static void main(String[] args) {
            for(int multiplicador = 1; multiplicador <= 10; multiplicador++) {
                for(int contador = 1; contador <= 10; contador++) {
                    System.out.println("Tabuada de "+ multiplicador+" x "+contador+" = "+ (multiplicador * contador));
                    
                }
                System.out.println();
            }
        }

}
